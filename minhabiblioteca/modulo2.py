def soma(num, num2):
    return "{} + {} = {}".format(num, num2, num+num2)

def diferenca(num, num2):
    return "{} - {} = ".format(num, num2, num-num2)

def produto(num, num2):
    return "{} * {} = {}".format(num, num2, num*num2)

def divisao(num, num2):
    return "{} / {} = {}".format(num, num2, num/num2)